package com.shristi.smart_traffic_light.Models;

public class Hospital {
    private int id;
    private double lat;
    private double lng;
    private String name;

    public int getId() {
        return id;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }

    public String getName() {
        return name;
    }
}
